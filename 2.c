#include <stdio.h>

 int main() {

    int num1, num2;

    printf("Digite o primeiro numero:" );
    scanf("%d", &num1);

    printf("Dgite o segundo numero:" );
    scanf("%d", &num2);

    printf("\nA soma dos numeros foi de:  %d", num1 + num2);
    printf("\nA subtração dos numeros foi de:  %d ", num1 - num2);
    printf("\nA multiplicação dos numeros foi de:  %d", num1 * num2);
    printf("\nA divisão dos numeros foi de: %d", num1 / num2);
 }
